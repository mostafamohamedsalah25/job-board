<script setup>
import { onMounted, ref } from 'vue';

const model = defineModel({
    type: String,
    required: true,
});

const input = ref(null);

onMounted(() => {
    if (input.value.hasAttribute('autofocus')) {
        input.value.focus();
    }
});

defineExpose({ focus: () => input.value.focus() });
</script>

<template>
    <input
        class="rounded-lg border-outline-variant bg-white text-sm shadow-sm focus:border-secondary focus:ring-secondary transition-colors"
        v-model="model"
        ref="input"
    />
</template>
